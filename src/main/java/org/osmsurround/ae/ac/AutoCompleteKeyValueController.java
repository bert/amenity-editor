package org.osmsurround.ae.ac;

import java.util.List;

import org.osmsurround.ae.dao.NodeValueResult;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.servlet.ModelAndView;

@Controller
public class AutoCompleteKeyValueController {

	@Autowired
	private AutoCompleteService autoCompleteService;

	@RequestMapping(value = "/ac/value", method = RequestMethod.GET)
	public ModelAndView get(@RequestParam String key, @RequestParam String input) {
		List<NodeValueResult> result = autoCompleteService.searchByKeyAndValue(key, input);
		return new ModelAndView("acKeyValueResult", "result", result);
	}
}
